package java2;

public class Main {

	public static void main(String[] args) {
//		iを1から9まで繰り返す
		for (int i = 1; i < 10; i++) {
//			jも1から9まで繰り返す
			for (int j = 1; j < 10; j++) {
				System.out.println(i * j);
//				空白を出力
				System.out.println(" ");
			}
//			改行を出力
			System.out.println(" ");
			}
		}
	}
	